def _step_description(step: dict[str, Any], with_background: bool) -> str:
    desc = step["step_description_prompt"]
    if with_background and step.get("step_background"):
        return desc + "\n" + step["step_background"]
    return desc


def _step_header_and_return(step: dict[str, Any]) -> str:
    header = step["function_header"]
    return_line = step.get("return_line") or ""
    return f"{header}\n\n{return_line}"


def _build_step_prompt(
    doc: dict[str, Any],
    step_idx: int,
    previous_llm_code: list[str | None],
    with_background: bool,
) -> str:
    """Build the prompt asking the model to generate sub-step ``step_idx``.

    ``previous_llm_code[i]`` must contain the code for sub-step ``i`` for all
    ``i < step_idx`` (either model-generated from an earlier call, or a
    hardcoded snippet for the upstream gold-code sub-steps).
    """
    sub_steps = doc["sub_steps"]
    output_lines: list[str] = []
    for i in range(step_idx):
        output_lines.append(_step_description(sub_steps[i], with_background))
        prev_code = previous_llm_code[i]
        if prev_code is not None:
            output_lines.append(prev_code)
        output_lines.append("------")
    if output_lines:
        output_lines = output_lines[:-1]

    next_step_lines = [
        _step_description(sub_steps[step_idx], with_background),
        _step_header_and_return(sub_steps[step_idx]),
    ]

    template = _WITH_BACKGROUND_PROMPT_TEMPLATE if with_background else _DEFAULT_PROMPT_TEMPLATE
    return template.format(
        problem_steps_str="\n\n".join(output_lines),
        next_step_str="\n\n".join(next_step_lines),
        dependencies=doc["required_dependencies"],
    )


def _strip_import_lines(code: str) -> str:
    kept = []
    for line in code.split("\n"):
        stripped = line.lstrip()
        if stripped.startswith("import ") or stripped.startswith("from "):
            continue
        kept.append(line)
    return "\n".join(kept)


@dataclass(frozen=True, slots=True)
class SciCodeExecutionScorer(ExecutionScorer):
    """Run each sub-step's tests against the model's generated code."""

    name: str = "scicode_exec"
    timeout: float = 600.0
    h5py_file: str = DEFAULT_H5PY_PATH

    async def ascore(
        self,
        instance: Instance,
        output: LMOutput,
        execution_env: ExecutionEnvironment,
    ) -> float:
        code = output.extracted_answer
        sub_steps = instance.metadata.get("sub_steps", [])
        deps = instance.metadata.get("required_dependencies", "")
        hardcoded_prelude = instance.metadata.get("hardcoded_prelude", "")
        scorable_steps = [s for s in sub_steps if not s.get("_hardcoded")]
        if not code or not scorable_steps:
            output.metadata["step_results"] = [False] * len(scorable_steps)
            output.metadata["passed_steps"] = 0
            output.metadata["total_steps"] = len(scorable_steps)
            return 0.0

        results: list[bool] = []
        for step in scorable_steps:
            script = self._build_step_script(step, deps, code, hardcoded_prelude)
            exec_result = await execution_env.execute_code(
                script, language="python", timeout=self.timeout
            )
            results.append(bool(exec_result.success))

        output.metadata["step_results"] = results
        output.metadata["passed_steps"] = sum(results)
        output.metadata["total_steps"] = len(scorable_steps)
        return float(sum(results)) / len(scorable_steps) if scorable_steps else 0.0

    def _build_step_script(
        self,
        step: dict[str, Any],
        deps: str,
        code: str,
        hardcoded_prelude: str = "",
    ) -> str:
        step_id = step["step_number"]
        test_cases = list(step.get("test_cases") or ())
        lines: list[str] = [deps.strip(), _H5_HELPER.strip()]
        if hardcoded_prelude.strip():
            lines.append(hardcoded_prelude.strip())
        lines.append(code.strip())
        lines.append(
            f"targets = process_hdf5_to_tuple({step_id!r}, {len(test_cases)}, {self.h5py_file!r})"
        )
        for idx, tc in enumerate(test_cases):
            lines.append(f"target = targets[{idx}]")
            lines.append(tc)
        return "\n\n".join(lines) + "\n"


@dataclass(frozen=True, slots=True)
class SciCodeSubStepAccuracyMetric(Metric):
    """Micro-averaged sub-step accuracy (passed / total across all sub-steps)."""

    name: str = "sub_step_accuracy"
    scorer: type[Scorer] = SciCodeExecutionScorer

    def compute(self, responses: Sequence[Response]) -> float:
        total = 0
        passed = 0
        for response in responses:
            for output in response.outputs:
                meta = output.metadata or {}
                total += int(meta.get("total_steps", 0))
                passed += int(meta.get("passed_steps", 0))
        return passed / total if total else 0.0


@dataclass(frozen=True, slots=True)
class SciCodeMainProblemAccuracyMetric(Metric):
    """Fraction of main problems where every sub-step passes."""

    name: str = "main_problem_accuracy"
    scorer: type[Scorer] = SciCodeExecutionScorer

    def compute(self, responses: Sequence[Response]) -> float:
        if not responses:
            return 0.0
        correct = 0
        for response in responses:
            if not response.outputs:
                continue
            output = response.outputs[0]
            meta = output.metadata or {}
            total = int(meta.get("total_steps", 0))
            passed = int(meta.get("passed_steps", 0))
            if total > 0 and passed == total:
                correct += 1
        return correct / len(responses)


@register("scicode")
class SciCode(Task):
    """SciCode multi-step scientific code generation task."""

    data_source = DataSource(path="SciCode1/SciCode", split="test")
    sampling_params = SamplingParams(max_tokens=16384, temperature=0.6)
    metrics = (
        SciCodeSubStepAccuracyMetric(),
        SciCodeMainProblemAccuracyMetric(),
    )
    primary_metric = SciCodeSubStepAccuracyMetric()
    with_background: bool = False

    @property
    def instances(self) -> Iterator[Instance]:
        if self._instances_cache is None:
            self._instances_cache = []
            loader = DataLoader()
            source = self.config.get_data_source()
            for doc in loader.load(source):
                instance = self.process_doc(doc)
                if instance is not None:
                    self._instances_cache.append(instance)
        yield from self._instances_cache

    def process_doc(self, doc: dict[str, Any], index: int = 0) -> Instance | None:
        problem_id = str(doc["problem_id"])
        sub_steps = [dict(s) for s in doc["sub_steps"]]
        if not sub_steps:
            return None

        hardcoded = _HARDCODED_SNIPPETS.get(problem_id, {})
        hardcoded_prelude_parts: list[str] = []
        for idx, step in enumerate(sub_steps):
            if idx in hardcoded:
                step["_hardcoded"] = True
                hardcoded_prelude_parts.append(hardcoded[idx])
        scorable_count = sum(1 for s in sub_steps if not s.get("_hardcoded"))

        with_background = getattr(self, "with_background", False)

        doc_for_prompt = {
            "sub_steps": sub_steps,
            "required_dependencies": doc["required_dependencies"],
        }
        first_idx = next(
            (i for i, s in enumerate(sub_steps) if not s.get("_hardcoded")),
            0,
        )
        previous_llm_code: list[str | None] = [
            hardcoded[i] if i < first_idx and i in hardcoded else None
            for i in range(len(sub_steps))
        ]
        prompt = _build_step_prompt(
            doc_for_prompt,
            step_idx=first_idx,
            previous_llm_code=previous_llm_code,
            with_background=with_background,
        )

        return Instance(
            question=prompt,
            gold_answer=doc.get("general_solution"),
            metadata={
                "id": problem_id,
                "problem_id": problem_id,
                "problem_name": doc.get("problem_name"),
                "required_dependencies": doc["required_dependencies"],
                "sub_steps": sub_steps,
                "total_steps": scorable_count,
                "hardcoded_prelude": "\n\n".join(hardcoded_prelude_parts),
                "first_step_idx": first_idx,
            },
        )

    def _build_fewshot(self) -> list[Instance]:
        return []

    def format_request(self, instance: Instance) -> LMRequest:
        if self.config.formatter is not None:
            return self.config.formatter.format(instance, [])
        return LMRequest(
            request_type=RequestType.CHAT,
            messages=({"role": "user", "content": instance.question},),
        )

    def extract_answer(self, output: LMOutput) -> str | None:
        code = extract_code(output.text)
        if not code:
            return None
        return _strip_import_lines(code)

    async def score_responses(
        self,
        responses: Sequence[Response],
        context: ScoringContext | None = None,
    ) -> Sequence[Response]:
        with_background = getattr(self, "with_background", False)
        provider_name = self._resolve_provider_name(context)

        for response in responses:
            if not response.outputs:
                continue
            instance = response.instance
            sub_steps = instance.metadata.get("sub_steps", [])
            problem_id = str(instance.metadata.get("problem_id", ""))
            hardcoded = _HARDCODED_SNIPPETS.get(problem_id, {})
            first_idx = int(instance.metadata.get("first_step_idx", 0))

            doc_for_prompt = {
                "sub_steps": sub_steps,
                "required_dependencies": instance.metadata.get("required_dependencies", ""),
            }

            previous_llm_code: list[str | None] = [None] * len(sub_steps)
            for idx, snippet in hardcoded.items():
                previous_llm_code[idx] = snippet

            output = response.outputs[0]
            step_texts: dict[int, str] = {}
            step_codes: dict[int, str] = {}

            first_code = _extract_step_code(output.text)
            step_texts[first_idx] = output.text
            step_codes[first_idx] = first_code
            previous_llm_code[first_idx] = first_code

            provider = None
            for idx in range(first_idx + 1, len(sub_steps)):
                if sub_steps[idx].get("_hardcoded"):
                    continue
                prompt = _build_step_prompt(
                    doc_for_prompt,
                    step_idx=idx,
                    previous_llm_code=previous_llm_code,
                    with_background=with_background,
                )
                if provider is None:
                    assert context is not None
                    provider = context.get_provider(provider_name)
                request = LMRequest(
                    request_type=RequestType.CHAT,
                    messages=({"role": "user", "content": prompt},),
                )
                results = await provider.agenerate([request], self.config.sampling_params)
                text = results[0][0].text if results and results[0] else ""
                code = _extract_step_code(text)
                step_texts[idx] = text
                step_codes[idx] = code
                previous_llm_code[idx] = code

            concatenated = "\n\n".join(step_codes[i] for i in sorted(step_codes) if step_codes[i])
            output.extracted_answer = concatenated
            if output.metadata is None:
                output.metadata = {}
            output.metadata["step_texts"] = step_texts
            output.metadata["step_codes"] = step_codes

        if context is not None:
            await self._apply_scorers_async(responses, context)
        else:
            self._apply_scorers(responses)
        return responses

    provider_name: str = "cascade"

    def _resolve_provider_name(self, context: ScoringContext | None) -> str:
        if context is None or context.inference_pool is None:
            raise RuntimeError(
                "SciCode sequential cascade requires a ScoringContext with an "
                "inference_pool. Configure an auxiliary provider named "
                f"{self.provider_name!r} (e.g. "
                f"-o auxiliary_providers.{self.provider_name}.kind=vllm_server "
                f"-o auxiliary_providers.{self.provider_name}.model=<same model>)."
            )
        names = context.inference_pool.names
        if self.provider_name not in names:
            raise RuntimeError(
                f"SciCode cascade requires an auxiliary provider named "
                f"{self.provider_name!r}. Available providers: {names!r}."
            )
        return self.provider_name


def _extract_step_code(text: str) -> str:
    if not text:
        return ""
    code = extract_code(text)
    if not code:
        return ""
    return _strip_import_lines(code)


