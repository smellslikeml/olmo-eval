"""SciCode benchmark: scientific multi-step code generation.

Reference: https://github.com/scicode-bench/SciCode (dataset SciCode1/SciCode).

SciCode asks a model to implement the Python functions required to solve
multi-step scientific research problems. Each main problem decomposes into a
sequence of sub-steps with numeric reference answers stored in
``test_data.h5``. Two reported scores:

- **Sub-step accuracy**: fraction of sub-steps whose tests pass.
- **Main-problem accuracy**: fraction of problems where every sub-step passes.

Implementation notes
--------------------
Each main problem is emitted as a single ``Instance``. The runner generates
the first sub-step via the normal request/response path; the remaining
sub-steps are generated sequentially inside ``score_responses`` using the
provider from the scoring context's inference pool. Each call's prompt
contains the problem description, every prior sub-step's description and
generated code, and the current sub-step's signature — mirroring the
upstream SciCode reference's cascading prompt flow.

The scorer then runs each sub-step's tests against the concatenation of all
model-generated code, records per-step pass/fail in
``output.metadata['step_results']``, and the two metrics read those results.

Three upstream sub-steps (13.6, 62.1, 76.3) ship with hardcoded gold snippets
in the SciCode repo rather than being generated by the model. Those snippets
are shown to the model as already-written code for later sub-steps, and are
injected verbatim into every test script (via ``hardcoded_prelude``) so that
dependent sub-steps can run. They are not scored — matching the upstream
denominator of 288 test / 50 validation sub-steps.

The scorer requires a sandbox that has ``numpy``, ``scipy``, ``sympy``, and
``h5py`` available, plus the SciCode numeric reference file at the path
configured via ``SciCodeExecutionScorer.h5py_file``. The scorer injects a
self-contained copy of the upstream ``process_hdf5_to_tuple`` helper into the
test script so the SciCode package does not need to be installed.
"""

